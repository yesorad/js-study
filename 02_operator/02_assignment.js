// 대입 연산자 ( Assignment Operator )
// - 변수에 값을 대입할 때 사용하는 이항 연산자

let a = 10 // 왼쪽 피연산자에 오른쪽 피연산자의 값을 대입
let b = 10 
let c = 10 
let d = 10 
let e = 10 

console.log(a -= 4) // 6 ( a = i - 4 )
console.log(b += 4) // 14 ( b = i + 4 )
console.log(c *= 4) // 40 ( c = i * 4 )
console.log(d /= 4) // 2.5 ( d = i / 4 )
console.log(e %= 4) // 2 ( e % 4 = 2 )